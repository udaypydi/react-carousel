describe('Infinite', () => {
  beforeEach(() => {
    cy.visit('/docs/examples/infinite');

    cy.get('.BrainhubCarouselItem--active')
      .children('img')
      .should('have.attr', 'src')
      .and('contain', 'mona');
  });

  it('repeats slides', () => {
    for (let i = 0; i < 17; i++) {
      cy.get('.BrainhubCarousel__arrowRight')
        .trigger('click');
    }

    cy.get('.BrainhubCarouselItem--active')
      .children('img')
      .should('have.attr', 'src')
      .and('contain', 'starry-night');
  });
});
